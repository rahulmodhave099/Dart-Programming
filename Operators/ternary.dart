// Ternary operators
// Ternary operators operates on three operands thus they are called as Ternary operator.
// Ternary operator is a one liner replacement for the if-else statement , as it is used to write conditional statements.
// syntax : 

void main(){
  int x = 10;
  int y = 20;

  (x<y) ? print("less than") : print("greater than");

  (x<y) ? print(x) : print(y);

  print((x<y) ? x : y);
}